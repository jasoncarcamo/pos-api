CREATE TABLE employees (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY AUTO INCRE,
    first_name TEXT NOT NULL,
    last_name TEXT NOT NULL,
    position TEXT NOT NULL,
    wage DECIMAL NOT NULL,
    pin INTEGER NOT NULL,
    isClockIn BOOlEAN DEFAULT FALSE NOT NULL
);

CREATE TABLE timesheets (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,           
    hours DECIMAL NOT NULL,
    date TIMESTAMP DEFAULT now() NOT NULL, 
    employee_id INTEGER REFERENCES employees(id) ON DELETE CASCADE NOT NULL
);

CREATE TABLE menu (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    category TEXT NOT NULL
);

CREATE TABLE menuItems (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    price DECIMAL NOT NULL,
    menuId INTEGER REFERENCES menu(id) ON DELETE CASCADE NOT NULL
);


